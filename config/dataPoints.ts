// Helper function for creating kebab-case IDs
const createId = (name: string): string => {
  if (typeof name !== 'string' || !name) {
    return ''; 
  }
  return name
    .toLowerCase()
    .replace(/\s+/g, '-') 
    .replace(/[^a-z0-9-_]+/g, '') 
    .replace(/^-+|-+$/g, '') 
    .replace(/-{2,}/g, '-'); 
};

